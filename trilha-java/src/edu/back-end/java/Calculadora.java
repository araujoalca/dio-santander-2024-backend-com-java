public class Calculadora {
    /**
     * Este método é responsável por realizar a soma de dois números inteiros.
     * @param numeroUm Primeiro número inteiro
     * @param numeroDois Segundo número inteiro
     * @return int Retorna a soma dos dois números inteiros
     */
    public int somar(int numeroUm, int numeroDois) {
        return numeroUm + numeroDois;
    }

}
